package com.michaelbahreini.interfacesoop;

public class TV {

    public void powerOn(){
        System.out.println("TV now powered on");
    }

    public void powerOff(){
        System.out.println("TV now powered off");
    }

    public void changeChannel(){
        System.out.println("changes the TV channel");
    }

    public void volumeUp(){
        System.out.println("TV volume up");
    }

    public void volumeDown(){
        System.out.println("TV volume down");
    }
}
